package example.modi.member.pac1;

public class A {
	//멤버변수 선언
	public int a;
	int b;
	private int c;
	
	//메서드 선언
	public void method1() {
		
	}
	void method2() {
		
	}
	private method3() {
		
	}

	//생성자 선언
	//생성자 안에 메서드 정의가 선언 가능.
	public A() {
		a=1;
		b=1;
		c=1;
		
		method1();
		method2();
		method3();
	}
}
